function showProduct(data){


    let products_container = document.querySelector(".products-container");
    
    
    data.forEach(item => {
        
        let maindiv = document.createElement("div");
        maindiv.setAttribute("class", "maindiv");
        
        //product sent to local storage for cart usage
        maindiv.onclick = function (){
            localStorage.setItem("productItem", JSON.stringify(item));
            window.location.href = "../cart.html";
        }
        
        let imagediv = document.createElement("div");
        imagediv.setAttribute("class", "imagediv");

        let img = document.createElement("img");
        img.src = item.image;

        
        let textdiv = document.createElement("div");
        textdiv.setAttribute("class", "textdiv");

        let pricediv = document.createElement("div");
        pricediv.setAttribute("class", "pricediv");

        let description = document.createElement("p");
        description.textContent = item.description;

        let h4 = document.createElement("h4");
        h4.setAttribute("class", "price");
        h4.textContent = `£ ${item.price}`;
        h4.style.textDecoration = "line-through";

        let dis = document.createElement("h4");
        dis.setAttribute("class", "price");
        dis.textContent = `(${item.discount})`;

        let h3 = document.createElement("h3");
        h3.setAttribute("class", "offprice");
        h3.textContent = `£ ${item.offPrice}`;
        h3.style.color = "red";
        


        imagediv.append(img);
        pricediv.append(h4, h3 , dis)
        textdiv.append(description, pricediv)
        maindiv.append(imagediv, textdiv);
        products_container.append(maindiv);
    })
}

